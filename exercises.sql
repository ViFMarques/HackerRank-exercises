ATIVIDADE 6  --Average Population of Each Continent

SELECT CONTINENT, FLOOR (AVG(CITY.POPULATION))
FROM CITY 
INNER JOIN COUNTRY
ON CITY.COUNTRYCODE = COUNTRY.CODE;
GROUP BY CONTINENT;


